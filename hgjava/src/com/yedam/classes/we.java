package com.yedam.classes;

public class we {
	private String name;
	private String yes;
	
	public we(String name, String yes) {
		super();
		this.name = name;
		this.yes = yes;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getYes() {
		return yes;
	}

	public void setYes(String yes) {
		this.yes = yes;
	}
	
	
}

